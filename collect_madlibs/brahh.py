def madlib():
    place = input('place: ')
    adjective = input('adjective: ')
    vehicle = input('vehicle: ')
    food = input('food: ')
    celebrity = input('celebrity: ')
    weird_thing = input('weird thing: ')

    madlib = f'My dream vacation would be to travel to {place}, ride a {adjective} {vehicle} across the sky, " \
          "and eat unlimited {food} while listening to {celebrity} sing lullabies about {weird_thing}."'
    print(madlib)
